namespace {
const SwitchCaseTarget *findCase(const SwitchCaseTarget *items, std::size_t count, int case_id) {
    std::size_t lo = 0;
    std::size_t hi = count;
    while (lo < hi) {
        std::size_t mid = lo + (hi - lo) / 2;
        if (items[mid].case_id == case_id) {
            return &items[mid];
        }
        if (items[mid].case_id < case_id) {
            lo = mid + 1;
        } else {
            hi = mid;
        }
    }
    return nullptr;
}
} // namespace

const SwitchCaseTarget *findPrimarySwitchCase(int case_id) {
    return findCase(kPrimarySwitchCases, kPrimarySwitchCaseCount, case_id);
}

